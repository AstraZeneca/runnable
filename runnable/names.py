def get_random_name(sep="-") -> str:
    """
    Returns a random name in the format of docker container names.

    Args:
        sep (str, optional): The seperator to use between the names. Defaults to '-'.

    Returns:
        str: The random name.
    """
    r = random.SystemRandom()
    name = "%s%s%s" % (r.choice(left), sep, r.choice(right))
    return name
